        // TODO add your handling code here:
        int selectedRow = tblDrugRequests.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a request to process");
            return;
        }

        DrugProductionRequest request = (DrugProductionRequest) tblDrugRequests.getValueAt(selectedRow, 0);
        ProcessDrugRequestsPanel processPanel = new ProcessDrugRequestsPanel(container,
                userAccount, organization, enterprise, system, request);
        container.add("ProcessDrugRequestsPanel", processPanel);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.next(container);

