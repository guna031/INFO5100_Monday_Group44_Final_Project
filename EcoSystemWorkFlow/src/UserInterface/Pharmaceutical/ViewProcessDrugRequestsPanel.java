    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblDrugRequests.getModel();
        model.setRowCount(0);

        for (WorkRequest request : organization.getWorkRequestList().getWorkRequests()) {
            if (request instanceof DrugProductionRequest) {
                DrugProductionRequest drugRequest = (DrugProductionRequest) request;
                Object[] row = new Object[7];
                row[0] = drugRequest;
                row[1] = drugRequest.getDrugName();
                row[2] = drugRequest.getQuantity();
                row[3] = drugRequest.getUrgency();
                row[4] = drugRequest.getStatus();
                row[5] = drugRequest.getDateOfRequest();
                row[6] = drugRequest.getSender().getEmployee().getEmpolyeeName();
                model.addRow(row);
            }
        }
    }
    
    

